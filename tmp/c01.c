// Ejemplo (rebuscado) del uso de operadores logicos
// Lee un numero, si es negativo imprime un "0" y si 
// es positivo un "1"; repite hasta que sea igual a "0"
{ bool z = true; int x; 

  for (; z ;) {
    read(x);
    if ((x > 0) && true)  print(1);          
    else if ((x < 0) || false) print(0);
         else if (! (x != 0) ) z = false;
              else {}
  }
}
